    private void BtnHapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnHapusMouseClicked
        // TODO add your handling code here:

        int brs =0 , jmlbaris=0, skstotal=0, sks = 0;
        double bobot, bobotsks=0, bobottotal=0, ips;
        String huruf;
        DefaultTableModel dataModel = (DefaultTableModel) TabelMhs.getModel();
        brs = TabelMhs.getSelectedRow();

        if (brs>=0) {
            int opsi = JOptionPane.showConfirmDialog(null, "Yakin dihapus?", "Peringatan", JOptionPane.YES_NO_OPTION);
            if (opsi == JOptionPane.YES_NO_OPTION) {
                //menghapus satu baris dalam tabel

                dataModel.removeRow(brs);
                jmlbaris = dataModel.getRowCount();
                if (jmlbaris<=0) {
                    TxtSKSTOTAL.setText("");
                    TxtIPS.setText("");
                }
                else {

                    //Menghitung ulang sks dan ips
                    jmlbaris= dataModel.getRowCount();
                    for (int i = 0; i < jmlbaris; i ++) {
                        //menghitung sks
                        sks = Integer.valueOf(dataModel.getValueAt(i,2).toString());
                        skstotal = skstotal + sks;

               
                       // menghitung ips
       
                    if (CmbNilai.getSelectedIndex()==1) 
                    {
                         bobot = 4.0;
                    }
                    else if (CmbNilai.getSelectedIndex()==2) 
                    {
                        bobot = 3.5;
                    }
                    else if (CmbNilai.getSelectedIndex()==3) 
                    {
                        bobot = 3.0;
                    }
                    else if (CmbNilai.getSelectedIndex()==4) 
                    {
                        bobot = 2.5;
                    }
                    else if (CmbNilai.getSelectedIndex()==5) 
                    {
                        bobot = 2.0;
                    }
                    else if (CmbNilai.getSelectedIndex()==6) 
                    {
                        bobot = 1.0;
                    }
                    else {
                        bobot =0.0;
                    }


                    bobotsks = bobot * sks;
                    bobottotal = bobottotal + bobotsks;

                }

                    TxtSKSTOTAL.setText(String.valueOf(skstotal));
                    ips = bobottotal / skstotal;
                    TxtIPS.setText(String.valueOf(ips));

                    }
                }
            }
    }//GEN-LAST:event_BtnHapusMouseClicked

