    private void BtnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTambahActionPerformed
        // TODO add your handling code here:
        int skstotal=0, sks=0, jmlbaris=0;
        double bobot, bobotsks=0, bobottotal=0, ips;
        String huruf;

        // memasukkan ke dalam table

        DefaultTableModel datamodel = (DefaultTableModel) TabelMhs.getModel();
        List list = new ArrayList<>();
        TabelMhs.setAutoCreateColumnsFromModel(true);
        list.add(JCMBMK.getSelectedItem());
        list.add(TxtKodeMatkul.getText());
        list.add(TxtSKS.getText());
        list.add(CmbNilai.getSelectedItem());
        
        
        datamodel.addRow(list.toArray());

        // menghitung total sks dan ips
        jmlbaris= datamodel.getRowCount();
        for (int i = 0; i < jmlbaris; i ++) {
            //menghitung sks
            sks = Integer.valueOf(datamodel.getValueAt(i,2).toString());
            skstotal = skstotal + sks;

            // menghitung ips
      
            if (CmbNilai.getSelectedIndex()==1) 
            {
                 bobot = 4.0;
            }
            else if (CmbNilai.getSelectedIndex()==2) 
            {
                bobot = 3.5;
            }
            else if (CmbNilai.getSelectedIndex()==3) 
            {
                bobot = 3.0;
            }
            else if (CmbNilai.getSelectedIndex()==4) 
            {
                bobot = 2.5;
            }
            else if (CmbNilai.getSelectedIndex()==5) 
            {
                bobot = 2.0;
            }
            else if (CmbNilai.getSelectedIndex()==6) 
            {
                bobot = 1.0;
            }
            else {
                bobot =0.0;
            }

            
            bobotsks = bobot * sks;
            bobottotal = bobottotal + bobotsks;

        }

        TxtSKSTOTAL.setText(String.valueOf(skstotal));
        ips = bobottotal / skstotal;
        TxtIPS.setText(String.valueOf(ips));

        //membersihkan
        TxtSKS.setText("");
        TxtKodeMatkul.setText("");
       

        JCMBMK.setSelectedIndex(0);
        CmbNilai.setSelectedIndex(0);
        BtnHapus.setEnabled(true);
        TxtSKS.setEnabled(false);
        
        
      
        BtnTambah.setEnabled(false);
        CmbNilai.setEnabled(false);
        JCMBMK.requestFocus();
    }//GEN-LAST:event_BtnTambahActionPerformed

