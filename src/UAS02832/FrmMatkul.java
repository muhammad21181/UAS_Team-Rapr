    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        // TODO add your handling code here:

        DefaultTableModel datamodel = (DefaultTableModel) TblMatkul.getModel();
        List list = new ArrayList<>();
        TblMatkul.setAutoCreateColumnsFromModel(true);
        list.add( TxtKodeMatkul.getText());
        list.add(TxtNamaMatkul.getText());
        list.add(CmbSks.getSelectedIndex());
        list.add( CmbSemester.getSelectedItem());
        datamodel.addRow(list.toArray());

        //Membersihkan data
        TxtKodeMatkul.setText("");
        TxtNamaMatkul.setText("");
        CmbSks.setSelectedIndex(0);
        CmbSemester.setSelectedIndex(0);

        //Menonaktifkan inputan
        TxtKodeMatkul.setEnabled(true);
        TxtNamaMatkul.setEnabled(false);
        CmbSemester.setEnabled(false);
        BtnHapus.setEnabled(true);
        BtnSave.setEnabled(false);
        CmbSks.setEnabled(false);
        TxtKodeMatkul.requestFocus();

    }//GEN-LAST:event_BtnSaveActionPerformed

