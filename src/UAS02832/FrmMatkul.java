    private void BtnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnResetActionPerformed
        // TODO add your handling code here:
        
        //membersihkan inputan
        
        TxtNamaMatkul.setText("");
        TxtKodeMatkul.setText("");
        btngroup.clearSelection();
        CmbSemester.setSelectedIndex(0);
       
        
        
        //menonaktifkan inputan txt
        
        TxtKodeMatkul.setEnabled(true);
        TxtNamaMatkul.setEnabled(false);
        BtnReset.setEnabled(false);
       BtnSave.setEnabled(false);
        CmbSemester.setEnabled(false);
       
        
         DefaultTableModel dataModel = (DefaultTableModel) TblMatkul.getModel();    
            if (TblMatkul.getRowCount() > 0) {
                for (int i = TblMatkul.getRowCount() - 1; i >= 0; i--) {
                    dataModel.removeRow(i);
                }
            }
            
        TxtKodeMatkul.requestFocus();
                
    }//GEN-LAST:event_BtnResetActionPerformed

